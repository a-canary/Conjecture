class MockDirtyFlagSystem:
    """Mock dirty flag system for testing."""
    
    def __init__(self):
        self.dirty_claims: Dict[str, Any] = {}
    
    def mark_claim_dirty(self, claim, reason, priority=0, cascade=True):
        claim.is_dirty = True
        claim.dirty = True
        self.dirty_claims[claim.id] = (reason, priority)
    
    def is_dirty(self, claim_id) -> bool:
        return claim_id in self.dirty_claims


