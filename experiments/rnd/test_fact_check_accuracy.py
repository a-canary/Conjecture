def mock_web_search(query: str) -> List[Tuple[str, str, str]]:
    """Mock web search that simulates real results."""
    # Check for known true facts
    true_facts = {
        "water boils at 100": [("Wikipedia: Boiling point", "https://example.com", "Water boils at 100°C at standard atmospheric pressure.")],
        "earth orbits the sun": [("NASA", "https://example.com", "The Earth orbits the Sun once every 365.25 days.")],
        "sum of angles in a triangle is 180": [("Math World", "https://example.com", "The sum of the interior angles of a triangle is 180 degrees.")],
        "pi is": [("Wikipedia", "https://example.com", "Pi is approximately 3.14159.")],
    }
    
    false_facts = {
        "water boils at 50": [("Science Daily", "https://example.com", "FALSE: Water boils at 100°C, not 50°C.")],
        "earth orbits the moon": [("NASA", "https://example.com", "FALSE: The Moon orbits the Earth, not the other way around.")],
    }
    
    query_lower = query.lower()
    
    # Check for true facts
    for key, results in true_facts.items():
        if key in query_lower:
            return results
    
    # Check for false facts
    for key, results in false_facts.items():
        if key in query_lower:
            return results
    
    # Default uncertain response
    return [("Web Search", "https://example.com", "Could not verify this claim with certainty.")]


