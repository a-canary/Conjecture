class MockVectorStore:
    """Simulates a vector store for testing purposes."""
    
    def __init__(self, claims: Dict[str, Claim]):
        self.claims = claims
        # Build a simple index based on content similarity detection
        self._build_index()
    
    def _build_index(self):
        """Build a simple keyword-based index for similarity."""
        self.index: Dict[str, List[Tuple[str, float]]] = {}
        claim_list = list(self.claims.values())
        
        for claim in claim_list:
            # Extract keywords
            words = set(claim.content.lower().split())
            for word in words:
                if len(word) > 3:  # Skip short words
                    if word not in self.index:
                        self.index[word] = []
                    self.index[word].append((claim.id, 1.0))
    
    def search(self, query: str, k: int = 10) -> List[Tuple[str, float]]:
        """Search for similar claims using keyword overlap."""
        query_words = set(query.lower().split())
        scores: Dict[str, float] = {}
        
        for word in query_words:
            if len(word) > 3 and word in self.index:
                for claim_id, _ in self.index[word]:
                    if claim_id not in scores:
                        scores[claim_id] = 0.0
                    scores[claim_id] += 1.0
        
        # Normalize by query length
        for claim_id in scores:
            scores[claim_id] /= len(query_words)
        
        # Sort by score and return top k
        sorted_results = sorted(scores.items(), key=lambda x: -x[1])[:k]
        return sorted_results


