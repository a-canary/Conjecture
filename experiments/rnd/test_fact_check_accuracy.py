def generate_test_claims(n_true=50, n_false=50) -> List[Tuple[Claim, bool]]:
    """Generate synthetic test claims with ground truth labels."""
    random.seed(42)
    claims = []
    
    # Select true claims
    true_sample = random.sample(TRUE_CLAIMS, min(n_true, len(TRUE_CLAIMS)))
    for content, domain in true_sample:
        claim = Claim(
            id=f"test_true_{len(claims)}",
            content=content,
            confidence=0.8,
            type=[ClaimType.OBSERVATION],
            tags=[domain, "test", "true"],
            state=ClaimState.VALIDATED,
            supers=[],
            subs=[],
        )
        claims.append((claim, True))  # True = is correct claim
    
    # Select and mutate false claims
    false_sample = random.sample(FALSE_CLAIM_TEMPLATES, min(n_false, len(FALSE_CLAIM_TEMPLATES)))
    for content, domain in false_sample:
        claim = Claim(
            id=f"test_false_{len(claims)}",
            content=content,
            confidence=0.8,
            type=[ClaimType.CONJECTURE],
            tags=[domain, "test", "false"],
            state=ClaimState.VALIDATED,
            supers=[],
            subs=[],
        )
        claims.append((claim, False))  # False = is incorrect claim
    
    # Pad if needed
    while len([c for c, _ in claims if c.tags[-1] == "true"]) < n_true:
        idx = len([c for c, _ in claims if c.tags[-1] == "true"])
        content, domain = TRUE_CLAIMS[idx % len(TRUE_CLAIMS)]
        claim = Claim(
            id=f"test_true_{len(claims)}",
            content=content + " (verified)",
            confidence=0.8,
            type=[ClaimType.OBSERVATION],
            tags=[domain, "test", "true"],
            state=ClaimState.VALIDATED,
            supers=[],
            subs=[],
        )
        claims.append((claim, True))
    
    while len([c for c, _ in claims if c.tags[-1] == "false"]) < n_false:
        idx = len([c for c, _ in claims if c.tags[-1] == "false"])
        content, domain = FALSE_CLAIM_TEMPLATES[idx % len(FALSE_CLAIM_TEMPLATES)]
        claim = Claim(
            id=f"test_false_{len(claims)}",
            content="NOT: " + content,
            confidence=0.8,
            type=[ClaimType.CONJECTURE],
            tags=[domain, "test", "false"],
            state=ClaimState.VALIDATED,
            supers=[],
            subs=[],
        )
        claims.append((claim, False))
    
    random.shuffle(claims)
    return claims[:n_true + n_false]


